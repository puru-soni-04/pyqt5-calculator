    def del_ac(self, value):
            global statement

            if value == 'del':
                    if statement == '':
                            self.Screen.setText('0')
                    elif statement[-4:-1] == "sin" or statement[-4:-1] == "cos" or statement[
                                                                                   -4:-1] == "tan" or statement[
                                                                                                      -4:-1] == "log":
                            statement = statement[0:-4]
                    else:
                            statement = statement[0:-1]

                    self.Screen.setText(str(statement))
                    if statement == '':
                            self.Screen.setText('0')

            elif value == 'ac':
                    statement = ''
                    self.Screen.setText('0')

